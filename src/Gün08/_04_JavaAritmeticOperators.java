package Gün08;

public class _04_JavaAritmeticOperators {
    public static void main(String[] args) {

        int a=9;
        int b=5;

        System.out.println("toplama islemi");
        System.out.println("a+b sonucu="+(a+b));

        System.out.println("cıkarma islemi");
        System.out.println("a-b sonucu="+(a-b));

        System.out.println("carpma islemi");
        System.out.println("a*b sonucu="+(a*b));

        System.out.println("bölme islemi");
        System.out.println("a/b sonucu="+(a/b));
        System.out.println("(double)a/b sonucu="+((double)a/b));

        //*******************

        System.out.println("Modül islemi:bölümünden kalan demek");
        System.out.println("a%b="+(a%b)); // 4
        System.out.println("b%a="+(b%a)); // 5





    }
}
