package Gün07;

public class _02_StringContains {
    public static void main(String[] args) {
        // contains= bir Stringin icerisinde karakter(lerin) var olup olmadıgını
        // boolean cinsinden söyle true ya da false

        String cümle="Merhaba Dünya";

        System.out.println("varMi a="+cümle.contains("a"));
        System.out.println("varMi ya="+cümle.contains("ya"));
        System.out.println("varMi z="+cümle.contains("z"));


    }
}
