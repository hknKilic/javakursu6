package Gün13;

public class _04_WhileLoop {

    public static void main(String[] args) {

        //Ekrana 5 kez merhaba yazdırınız.
        int sayac=1;
        while (sayac<=5){
            System.out.println(sayac+".Merhaba");
            sayac++; //sayac+1 demek

            System.out.println("program bitti");
        }

    }
}
